  private void LoginActionPerformed(java.awt.event.ActionEvent evt) {                                      
    String usuario = Usuario.getText();
    String contrasena = new String(jPasswordField1.getPassword());

    // Verifica las credenciales
    if (usuario.equals("admin") && contrasena.equals("password")) {
        // Si las credenciales son correctas, abre el JFrame del menú principal
        _menu_ menuPrincipal = new _menu_();
        menuPrincipal.setVisible(true);
        this.dispose(); // Cierra la ventana de login
    } else {
        // Si las credenciales son incorrectas, muestra un mensaje de error
        JOptionPane.showMessageDialog(this, "Credenciales incorrectas. Inténtelo de nuevo.", "Error de inicio de sesión", JOptionPane.ERROR_MESSAGE);
    }
} 
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
